CREATE TABLE ship_tier (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  tier DOUBLE PRECISION NOT NULL,
  tier_text TEXT NOT NULL,
  BP INTEGER NOT NULL,
  special TEXT
);

CREATE TABLE ship_frame (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  class TEXT NOT NULL,
  size TEXT NOT NULL,
  maneuverability TEXT NOT NULL,
  HP INTEGER NOT NULL,
  HP_increment INTEGER NOT NULL,
  DT INTEGER,
  CT INTEGER,
  expansion_bays INTEGER NOT NULL,
  min_crew INTEGER NOT NULL,
  max_crew FLOAT NOT NULL,
  BP_cost INTEGER NOT NULL,
  special_abilities TEXT,
  source TEXT
);